
public class Log {
    private int x, y;
    private int dx, dy;

    public Log() {
        x = 0;
        y = 0;
    }

    public int getX() {
        return x;
    }
    
    public int getDX() {
        return dx;
    }
    
    public int getDY() {
        return dy;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setDx(int d) {
        this.dx = d;
    }

    public void setDy(int d) {
        this.dy = d;
    }

    public int getY() {
        return y;
    }

    public void setY(int num) {

        this.y = num;
    }

}
